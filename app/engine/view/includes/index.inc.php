<h1>About</h1>
</p>
<p> 
<i>Open Validation Server</i> is a free online service for validating IGC files, used by the 
<a href="http://wxc.fai.org/" target="_blank">FAI CIVL World XC Online Contest</a> and
many other national and international flying contests.
</p>
<p>
<a href="https://www.fai.org/commission/igc" target="_blank">IGC</a> files are required to include a special file signature, which can be validated later by using a program
provided by the instrument manufacturer or software author. This service makes use of these programs
to check that IGC file data has not been altered.<br>
The IGC file format has been developed by members of the FAI IGC (gliding commission) in December 1994.
The old draft specification is available as a PDF download <a href="/documents/IGC-Spec_v1.00.pdf" target="_blank">here</a>.
Please note, the old draft is (as the name would say really old.) Please use latest IGC spec and CIVL draft for your development. 
The draft is intended to facilitate the introduction of GPS technology into IGC gliding and gliding competition verification, 
to create a standard for <a href="https://www.fai.org/page/igc-documents" target="_blank">IGC-Approved Flight Recorders</a> (GNSS).
</p>
<p>
The <i>Open Validation Server</i> project started in 2003 for the HG/PG Onlinecontest at Segelflugszene. (www.onlinecontest.org) 
The main goal was, to allow the use of IGC files on onlinecontest systems without the need to buy expensive GNSS
loggers at hanggliding / paragliding szene.
</p>
<p>
The <i>Open Validation Server</i> is free to use for any onlinecontest who will find it useful but without warranty.
</p>
<p>
At FAI CIVL World Online Contest level (WXC) we approve Software by testing it against minimum requirements 
to ensure stability for Servers and Pilot client PC software. A 3-letter code starting with X character is assigned 
to the software, outside from FAI GNSS logger approval process.
</p>

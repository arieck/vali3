<i>Notes:</i>
</p>
<p>
The Fédération Aéronautique Internationale (<a href="http://www.fai.org/" target="_blank">FAI</a>) is the World Air Sports Federation and was founded in 1905. 
It is a non-governmental and non-profit making international organisation with the basic aim of furthering 
aeronautical and astronautical activities worldwide, ratifying world and continental records and coordinating 
the organization of international competitions.
</p>
<p>
The FAI Hang Gliding and Paragliding Commission of the FAI (<a href="https://new.fai.org/commission/civl" target="_blank">CIVL</a>) conducts FAI's hang gliding and paragliding
activities in particular World Records and International Competitions.
</p>
<p>
The FAI Gliding Commission (<a href="https://www.fai.org/commission/igc" target="_blank">IGC</a>) conducts FAI's gliding activities (sailplane) 
in particular World Records and International Competitions with the exception of glider aerobatics. 
</p>